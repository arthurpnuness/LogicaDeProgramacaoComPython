
## Instrução ao usuário
print('Neste programa vamos te dizer o total de meses e dias que tu ja viveu')

## Interação com o usuário
idade = int(input('Digite a sua idade: '))

## Calculos e resultados
mesesViveu = idade * 12
print('Voce ja viveu {} meses'.format(mesesViveu))
diasViveu = idade * 365
print('Voce ja viveu {} dias'.format(diasViveu))
